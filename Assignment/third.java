
// Write a java program to generate multiplication tables from 2 to 5.

public class third {
    public static void main(String[] args) {

        for(int i = 2; i<=5; i++)
        {   System.out.println();
            for(int j = 1; j<= 10; j++)
            {
                System.out.println(i+" x "+j+" = "+i*j);
            }
        }
        
    }
    
}
