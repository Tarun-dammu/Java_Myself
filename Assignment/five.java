// Write a java program to demonstrate  if-else-if ladder.

public class five {
    public static void main(String[] args) {
        
        int a = -10;

        if(a>0)
        {
            System.out.println(a+" is a positive number");
        }       
        else if(a == 0)
        {
            System.out.println(a+" is a null number");
        }
        else
        {
            System.out.println(a+" is a negative number");
        }
    }
    
}
