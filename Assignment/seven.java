// Write a java program to find the sum of positive numbers by using while loop.
public class seven {
    public static void main(String[] args) {
        
        int n = 3;
        int sum = 0;
        int c = 0;

        while(c<=n)
        {
            sum = sum + c;
            c++;
        }
        System.out.println("Sum of Positive number : " + sum);

    }
    
    
}
