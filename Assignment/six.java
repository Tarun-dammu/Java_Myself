// Write a java program to check whether a given number is Even or Odd.

public class six {
    public static void main(String[] args) {
        
        int a = 10;

        if(a%2 == 0)
        {
            System.out.println(a+" is a even number");
        }       

        else
        {
            System.out.println(a+" is a odd number");
        }
    }
    
}
